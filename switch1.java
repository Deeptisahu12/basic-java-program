public class switch1 {
    public static void main(String[] args) {
        int x = 30;
        switch (x) {
            case 20:
                System.out.println("twenty");
            case 50:
                System.out.println("fifty");
            case 0:
                System.out.println("zero");
        }
        System.out.println("out of switch");
    }
}